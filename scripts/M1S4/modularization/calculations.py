def soma(valor_1, valor_2):
    """
    Retorna a soma de dois valores.

    Parâmetros:
        valor_1: Primeiro número.
        valor_2: Segundo número.

    Retorna:
        Soma dos dois valores.
    """
    return valor_1 + valor_2

def subtracao(valor_1, valor_2):
    """
    Retorna a subtração entre dois valores.

    Parâmetros:
        valor_1: Minuendo.
        valor_2: Subtraendo.

    Retorna:
        Diferença entre valor_1 e valor_2.
    """
    return valor_1 - valor_2

def divisao(valor_1, valor_2):
    """
    Retorna a divisão entre dois valores.

    Parâmetros:
        valor_1: Dividendo.
        valor_2: Divisor.

    Retorna:
        Resultado da divisão de valor_1 por valor_2.
    """
    return valor_1 / valor_2

def multiplicacao(valor_1, valor_2):
    """
    Retorna a multiplicação de dois valores.

    Parâmetros:
        valor_1: Primeiro fator.
        valor_2: Segundo fator.

    Retorna:
        Produto dos dois valores.
    """
    return valor_1 * valor_2

def potenciacao(valor_1, valor_2):
    """
    Retorna a potenciação de dois valores.

    Parâmetros:
        valor_1: Base.
        valor_2: Expoente.

    Retorna:
        valor_1 elevado à potência valor_2.
    """
    return valor_1 ** valor_2

def modulo(valor_1, valor_2):
    """
    Retorna o resto da divisão entre dois valores.

    Parâmetros:
        valor_1: Dividendo.
        valor_2: Divisor.

    Retorna:
        Resto da divisão de valor_1 por valor_2.
    """
    return valor_1 % valor_2
